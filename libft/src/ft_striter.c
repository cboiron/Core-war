#include "libft.h"

void	ft_striter(char *s, void (*f)(char *))
{
	int index;

	index = 0;
	if (s != 0 && f != 0)
	{
		while (s[index] != '\0')
		{
			f(&s[index]);
			index++;
		}
	}
}
